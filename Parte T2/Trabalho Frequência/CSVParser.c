void CSVParser_init(CSVParser *csv) {
    csv->state = START;
    for(int i=0; i<CSV_MAX_ROW_SIZE; i++)
        csv->buf[i] = '\0';
    csv->buflen = 0;
    csv->fields[0] = csv->buf;
    csv->fieldCount = 0;
}

void CSVParser_processLines(CSVParser* csv, const char* input, int inputSize, void(*callback)(char**,int,void* userData), void* userData) {
    int state = csv->state;
    char c;
    while(state && (inputSize--) && (c=*input++)) {
        int nextState = mte[state][c];
        if((state == QUOTED_STR && nextState != SEP_OR_NEWLINE) || (state == QUOTED_STR_SPECIAL_CHAR) || nextState == STR)
            csv->buf[csv->buflen++] = c;
        else if(nextState == ADD_FIELD || nextState == ADD_FIELD_AND_RECORD) {
            csv->buf[csv->buflen++] = '\0';
            csv->fieldCount++;
            csv->fields[csv->fieldCount] = csv->buf + csv->buflen;
            if(nextState == ADD_FIELD_AND_RECORD) {
                csv->buflen=0;
                if(csv->fieldCount)
                    callback(csv->fields,csv->fieldCount,userData);
                csv->fieldCount=0;
            }
            nextState = START;
        }
        state = nextState;
    }
    csv->state = state;
}
